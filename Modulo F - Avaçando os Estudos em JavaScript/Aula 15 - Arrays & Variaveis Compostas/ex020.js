let estacionamento = ['vaga1', 'vaga2', 'vaga3', 'vaga4', 'vaga5']
console.log(`${estacionamento} \n -------------------------`)

estacionamento.pop() // remove o ultimo elemento do array
console.log(`${estacionamento} \n -------------------------`)

estacionamento.push('adicionando vaga no final do array') // adiciona um elemento no final do array
console.log(`${estacionamento} \n -------------------------`)

estacionamento.shift() // remove o primeiro elemento do array
console.log(`${estacionamento} \n -------------------------`)

estacionamento.unshift('adicionando vaga no inicio do array') // adiciona um elemento no inicio do array
console.log(`${estacionamento} \n -------------------------`)


